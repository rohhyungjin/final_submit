function drawEndingScreen() {
  background(0); // 검은 배경
  
  if (creditY === 0) {
    creditY = windowHeight + 50; // 처음 시작은 화면 맨 아래 밖에서부터
  }

  textAlign(CENTER, CENTER);
  
  // 크레딧 텍스트 그리기
  for (let i = 0; i < endingTexts.length; i++) {
    // 텍스트 위치 계산
    let textY = creditY + i * 60;
    
    // 화면 범위 안에 있을 때만 그리기 (최적화)
    if (textY > -100 && textY < windowHeight + 100) {
      fill(255); // 흰색 글씨
      noStroke();
      textSize(30);
      text(endingTexts[i], windowWidth / 2, textY);
    }
  }

  // 텍스트 천천히 위로 올라감 (속도 조절 가능)
  creditY -= 1.5; 

  // 스킵/로비로 돌아가기 버튼 (화면 우측 하단 고정)
  fill(50, 50, 50, 200);
  stroke(255);
  strokeWeight(2);
  rect(windowWidth - 200, windowHeight - 70, 170, 45, 10);
  
  fill(255);
  noStroke();
  textSize(18);
  text("🔙 로비로 돌아가기", windowWidth - 115, windowHeight - 47);
}

function mousePressedEnding(mx, my) {
  if (mx > windowWidth - 200 && mx < windowWidth - 30 &&
      my > windowHeight - 70 && my < windowHeight - 25) {
    partyEmit("changeGameState", 2); 
    creditY = 0; 
  }
}
